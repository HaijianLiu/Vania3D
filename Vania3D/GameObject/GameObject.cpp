
#include "Engine.hpp"

/*------------------------------------------------------------------------------
< Constructor >
------------------------------------------------------------------------------*/
GameObject::GameObject() {

}


/*------------------------------------------------------------------------------
< Destructor >
------------------------------------------------------------------------------*/
GameObject::~GameObject() {
	deleteMap(this->components);
}


/*------------------------------------------------------------------------------
< start >
------------------------------------------------------------------------------*/
void GameObject::start() {
	for (auto it = this->components.begin(); it != this->components.end(); it++) {
		it->second->start();
	}
}


/*------------------------------------------------------------------------------
< update >
------------------------------------------------------------------------------*/
void GameObject::update() {
	for (auto it = this->components.begin(); it != this->components.end(); it++) {
		it->second->update();
	}
}
