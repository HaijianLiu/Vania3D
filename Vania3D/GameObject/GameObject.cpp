
#include "Engine.hpp"

/*------------------------------------------------------------------------------
< Constructor >
------------------------------------------------------------------------------*/
GameObject::GameObject() {

}


/*------------------------------------------------------------------------------
< Destructor >
------------------------------------------------------------------------------*/
GameObject::~GameObject() {
	deleteMap(this->components);
}


/*------------------------------------------------------------------------------
< start >
------------------------------------------------------------------------------*/
void GameObject::start() {
	for (const auto & conponent : this->components)
		conponent.second->start();
}


/*------------------------------------------------------------------------------
< update >
------------------------------------------------------------------------------*/
void GameObject::update() {
	for (const auto & conponent : this->components)
		conponent.second->update();
}
